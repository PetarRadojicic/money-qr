// Helper function to get icons by category
export const getIconsByCategory = (category: string) => {
  return categoryIcons.filter(icon => icon.category === category);
};

// Helper function to get all categories
export const getAllCategories = () => {
  const categories = [...new Set(categoryIcons.map(icon => icon.category))];
  return categories.sort();
};
